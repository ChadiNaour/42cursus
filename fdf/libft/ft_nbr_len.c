#include "libft.h"

int		ft_nbr_len(int f)
{
	int	i;

	i = 0;
	if (f < 0)
	{
		i++;
		f = f * (-1);
	}
	while (f != 0)
	{
		f = f / 10;
		i++;
	}
	return (i);
}
