#include "libft.h"

static int	ft_word_len(const char *s, char c)
{
	int i;
	int j;

	j = 0;
	i = 0;
	if (s[0] != c)
		j++;
	while (s[i] != '\0')
	{
		if (s[i] == c && s[i + 1] != c && s[i + 1] != '\0')
			j++;
		i++;
	}
	return (j);
}
