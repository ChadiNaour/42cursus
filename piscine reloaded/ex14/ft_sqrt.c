int		ft_sqrt(int nb)
{
	int i;

	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	else
	{
		i = 0;
		while (i <= 46340 && i <= nb / 2)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
		return (0);
	}
}
